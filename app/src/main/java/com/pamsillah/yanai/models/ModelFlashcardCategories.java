package com.pamsillah.yanai.models;

public class ModelFlashcardCategories {
    String id;
    String categoryName;

    public ModelFlashcardCategories(String id, String categoryName) {
        this.id = id;
        this.categoryName = categoryName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }
}
